	 Open-DAI (Opening Data Architectures and Infrastructures of European Public Administrations) &egrave; un progetto finanziato nell&rsquo;ambito del programma di politiche di support all&rsquo;ICT, come parte del bando EU 2011 del programma quadro  &ldquo;Competitivit&agrave; e Innovazione&rdquo; (CIP).<br/>
La durata del progetto &egrave; prevista dal 2012 al 2014. Al progetto collaborano 11 partner di 4 stati europei (Italia, spagna, Svezia e Turchia). Tra i partner, CSI Piemonte ha il ruolo di leader del progetto. <br/>
<h2>Il progetto Open-DAI e la Regione Piemonte</h2>
Regione Piemonte &egrave; partner del progetto Open DAI, e CSI Piemonte &egrave; il leader del progetto stesso. SaveUs &egrave; uno dei pilot implementati nell&rsquo;ambito del progetto, ovvero proposte di soluzioni creative e flessibili, rese possibili dalle nuove tecnologie, in contrapposizione ai sistemi complessi e statici attualmente in uso nelle pubbliche amministrazioni.
